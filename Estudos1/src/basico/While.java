
public class While {

	public static void main(String[] args) {
		
		/* WHILE
		 * A ideia é que esse trecho de código seja repetido enquanto uma determinada condição
			permanecer verdadeira.
		 */
		
		int idade = 15;
		
		while (idade <= 18) {
			System.out.println(idade);
			idade = idade + 1;
		}
		
		

	}

}
